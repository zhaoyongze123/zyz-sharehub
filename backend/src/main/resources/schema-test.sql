ALTER TABLE notes
  ADD COLUMN IF NOT EXISTS category VARCHAR(64);

ALTER TABLE notes
  ADD COLUMN IF NOT EXISTS is_official BOOLEAN NOT NULL DEFAULT FALSE;

ALTER TABLE notes
  ADD COLUMN IF NOT EXISTS is_pinned BOOLEAN NOT NULL DEFAULT FALSE;

CREATE TABLE IF NOT EXISTS reports (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  target_type VARCHAR(32) NOT NULL,
  target_id BIGINT NOT NULL,
  reporter_key VARCHAR(128) NOT NULL,
  reason VARCHAR(255),
  details CLOB,
  status VARCHAR(32) NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE,
  resolved_at TIMESTAMP WITH TIME ZONE,
  resolved_by VARCHAR(128)
);

CREATE TABLE IF NOT EXISTS admin_audit_logs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  action VARCHAR(64) NOT NULL,
  target_type VARCHAR(32) NOT NULL,
  target_id VARCHAR(128) NOT NULL,
  operator_key VARCHAR(128) NOT NULL,
  details CLOB NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS resumes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  template_key VARCHAR(128) NOT NULL,
  owner_key VARCHAR(128) NOT NULL DEFAULT 'local-dev-user',
  status VARCHAR(32) NOT NULL,
  file_id UUID NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE,
  updated_at TIMESTAMP WITH TIME ZONE
);

CREATE INDEX IF NOT EXISTS idx_files_roadmap_node_reference
  ON files(reference_type, reference_id, category, created_at);

CREATE INDEX IF NOT EXISTS idx_note_view_history_user_viewed_at
  ON note_view_history(user_key, viewed_at DESC);

CREATE INDEX IF NOT EXISTS idx_note_view_history_note_id
  ON note_view_history(note_id);

CREATE INDEX IF NOT EXISTS idx_roadmap_enrollments_user_status_updated
  ON roadmap_enrollments(user_key, status, updated_at DESC);

CREATE INDEX IF NOT EXISTS idx_roadmap_enrollments_roadmap_user
  ON roadmap_enrollments(roadmap_id, user_key);

CREATE INDEX IF NOT EXISTS idx_roadmap_node_progress_user_roadmap_status
  ON roadmap_node_progress(user_key, roadmap_id, status);

CREATE INDEX IF NOT EXISTS idx_roadmap_node_progress_roadmap_user_status
  ON roadmap_node_progress(roadmap_id, user_key, status);

CREATE INDEX IF NOT EXISTS idx_notes_owner_category
  ON notes(owner_key, category);

CREATE INDEX IF NOT EXISTS idx_notes_community_sort
  ON notes (is_pinned DESC, id DESC)
  WHERE visibility = 'PUBLIC' AND status = 'PUBLISHED';

CREATE INDEX IF NOT EXISTS idx_roadmaps_owner_status_id_desc
  ON roadmaps(owner_key, status, id DESC);

CREATE INDEX IF NOT EXISTS idx_notes_owner_status_id_desc
  ON notes(owner_key, status, id DESC);

CREATE INDEX IF NOT EXISTS idx_notes_visibility_status_pinned_id_desc
  ON notes(visibility, status, is_pinned DESC, id DESC);

CREATE INDEX IF NOT EXISTS idx_resources_owner_status_visibility_updated_desc
  ON resources(owner_key, status, visibility, updated_at DESC);

CREATE INDEX IF NOT EXISTS idx_resources_status_visibility_updated_desc
  ON resources(status, visibility, updated_at DESC);

CREATE UNIQUE INDEX IF NOT EXISTS ux_tags_slug
  ON tags(slug);

CREATE INDEX IF NOT EXISTS idx_resource_tags_tag_resource
  ON resource_tags(tag_id, resource_id);

CREATE INDEX IF NOT EXISTS idx_note_tags_tag_note
  ON note_tags(tag_id, note_id);

CREATE INDEX IF NOT EXISTS idx_reports_target_target_status_created_desc
  ON reports(target_type, target_id, status, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_reports_status_target_type_id_desc
  ON reports(status, target_type, id DESC);

CREATE UNIQUE INDEX IF NOT EXISTS ux_likes_user_resource
  ON likes(user_key, resource_id)
  WHERE resource_id IS NOT NULL AND note_id IS NULL;

CREATE UNIQUE INDEX IF NOT EXISTS ux_likes_user_note
  ON likes(user_key, note_id)
  WHERE note_id IS NOT NULL AND resource_id IS NULL;

CREATE UNIQUE INDEX IF NOT EXISTS ux_favorites_user_resource
  ON favorites(user_key, resource_id)
  WHERE resource_id IS NOT NULL AND note_id IS NULL;

CREATE UNIQUE INDEX IF NOT EXISTS ux_favorites_user_note
  ON favorites(user_key, note_id)
  WHERE note_id IS NOT NULL AND resource_id IS NULL;

CREATE INDEX IF NOT EXISTS idx_comments_parent_created_at_id
  ON comments(parent_id, created_at, id);

