CREATE TABLE IF NOT EXISTS files (
  id UUID PRIMARY KEY,
  owner VARCHAR(128) NOT NULL,
  category VARCHAR(32) NOT NULL,
  reference_type VARCHAR(64) NOT NULL,
  reference_id VARCHAR(128) NOT NULL,
  filename VARCHAR(255) NOT NULL,
  content_type VARCHAR(255),
  size BIGINT NOT NULL,
  checksum VARCHAR(64) NOT NULL,
  data BYTEA NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE,
  updated_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS resources (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title VARCHAR(255) NOT NULL,
  type VARCHAR(32),
  summary CLOB,
  tags CLOB,
  external_url CLOB,
  object_key VARCHAR(256),
  owner_key VARCHAR(128) NOT NULL DEFAULT 'local-dev-user',
  visibility VARCHAR(32),
  status VARCHAR(32),
  created_at TIMESTAMP WITH TIME ZONE,
  updated_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  login VARCHAR(128) NOT NULL UNIQUE,
  name VARCHAR(255),
  bio CLOB,
  avatar_file_id UUID,
  status VARCHAR(32) NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE,
  updated_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS admin_whitelist (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  github_login VARCHAR(128) NOT NULL UNIQUE,
  role VARCHAR(32) NOT NULL,
  created_by VARCHAR(128) NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE,
  updated_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS roadmaps (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title VARCHAR(255) NOT NULL,
  description CLOB,
  owner_key VARCHAR(128) NOT NULL DEFAULT 'local-dev-user',
  visibility VARCHAR(32),
  status VARCHAR(32),
  created_at TIMESTAMP WITH TIME ZONE,
  updated_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS roadmap_nodes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  roadmap_id BIGINT NOT NULL,
  parent_id BIGINT,
  title VARCHAR(255) NOT NULL,
  description CLOB,
  order_no INTEGER,
  resource_id BIGINT,
  note_id BIGINT,
  created_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS roadmap_progress (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  roadmap_id BIGINT NOT NULL,
  user_key VARCHAR(128) NOT NULL,
  payload CLOB NOT NULL,
  updated_at TIMESTAMP WITH TIME ZONE,
  CONSTRAINT uk_roadmap_progress UNIQUE (roadmap_id, user_key)
);

CREATE TABLE IF NOT EXISTS roadmap_enrollments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  roadmap_id BIGINT NOT NULL,
  user_key VARCHAR(128) NOT NULL,
  status VARCHAR(32) NOT NULL,
  started_at TIMESTAMP WITH TIME ZONE,
  completed_at TIMESTAMP WITH TIME ZONE,
  created_at TIMESTAMP WITH TIME ZONE,
  updated_at TIMESTAMP WITH TIME ZONE,
  CONSTRAINT uk_roadmap_enrollments UNIQUE (roadmap_id, user_key),
  CONSTRAINT chk_roadmap_enrollments_status
    CHECK (status IN ('ACTIVE', 'PAUSED', 'COMPLETED', 'QUIT'))
);

CREATE TABLE IF NOT EXISTS roadmap_node_progress (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  roadmap_id BIGINT NOT NULL,
  node_id BIGINT NOT NULL,
  user_key VARCHAR(128) NOT NULL,
  status VARCHAR(32) NOT NULL,
  completed_at TIMESTAMP WITH TIME ZONE,
  created_at TIMESTAMP WITH TIME ZONE,
  updated_at TIMESTAMP WITH TIME ZONE,
  CONSTRAINT uk_roadmap_node_progress UNIQUE (node_id, user_key),
  CONSTRAINT chk_roadmap_node_progress_status
    CHECK (status IN ('NOT_STARTED', 'IN_PROGRESS', 'COMPLETED', 'SKIPPED'))
);

CREATE TABLE IF NOT EXISTS notes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title VARCHAR(255) NOT NULL,
  content_md CLOB NOT NULL,
  owner_key VARCHAR(128) NOT NULL DEFAULT 'local-dev-user',
  visibility VARCHAR(32),
  status VARCHAR(32),
  category VARCHAR(64),
  is_official BOOLEAN NOT NULL DEFAULT FALSE,
  is_pinned BOOLEAN NOT NULL DEFAULT FALSE,
  created_at TIMESTAMP WITH TIME ZONE,
  updated_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS tags (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(128) NOT NULL,
  slug VARCHAR(160) NOT NULL,
  type VARCHAR(32) NOT NULL,
  status VARCHAR(32) NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE,
  updated_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS resource_tags (
  resource_id BIGINT NOT NULL,
  tag_id BIGINT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE,
  CONSTRAINT pk_resource_tags PRIMARY KEY (resource_id, tag_id)
);

CREATE TABLE IF NOT EXISTS note_tags (
  note_id BIGINT NOT NULL,
  tag_id BIGINT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE,
  CONSTRAINT pk_note_tags PRIMARY KEY (note_id, tag_id)
);

CREATE TABLE IF NOT EXISTS comments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  resource_id BIGINT,
  note_id BIGINT,
  parent_id BIGINT,
  author_key VARCHAR(128) NOT NULL,
  content CLOB NOT NULL,
  status VARCHAR(32) NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS favorites (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  resource_id BIGINT,
  note_id BIGINT,
  user_key VARCHAR(128) NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS likes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  resource_id BIGINT,
  note_id BIGINT,
  user_key VARCHAR(128) NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS note_view_history (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  note_id BIGINT NOT NULL,
  user_key VARCHAR(128) NOT NULL,
  viewed_at TIMESTAMP WITH TIME ZONE NOT NULL,
  CONSTRAINT uk_note_view_history_note_user UNIQUE (note_id, user_key)
);

